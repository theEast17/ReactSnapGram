export async function createUserAccount(user: INewUser) {
  try {
    // step 1 create new account
    const newAccount = await account.create(
      ID.unique(),
      user.email,
      user.password,
      user.name
    );

    if (!newAccount) throw Error;

    // step 2 store data into the database

    const avatarUrl = avatars.getInitials(user.name); // it will create a something like image that appwrite can put if we doesnt pass any url  if name is bhavin than your user Profile image would be ' B '

    const newUser = await saveUserToDB({
      accountId: newAccount.$id,
      name: newAccount.name,
      email: newAccount.email,
      username: user.username,
      imageUrl: avatarUrl,
    });
    return newUser; // here you will get yhe new user and it will be stored into the database
  } catch (error) {
    console.log(error);
    return error;
  }
}

